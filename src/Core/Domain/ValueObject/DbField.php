<?php

declare(strict_types=1);

namespace App\Core\Domain\ValueObject;

final class DbField
{
    private string $value;

    /**
     * @param string $value
     */
    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }
}
